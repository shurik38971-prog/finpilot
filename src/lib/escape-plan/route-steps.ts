export interface EscapeRouteStep {
  title: string;
  description: string;
  /** User-facing benefit; stored in financial_tasks.explanation */
  why_important: string;
}

function step(
  title: string,
  description: string,
  why_important: string
): EscapeRouteStep {
  return { title, description, why_important };
}

function isOnSiteRepairRoute(title: string): boolean {
  return /сантех|ремонт.*выезд|выезд.*ремонт|мастер.*выезд/i.test(title);
}

