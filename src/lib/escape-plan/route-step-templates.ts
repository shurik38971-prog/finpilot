export function buildStepsForRouteType(
  routeType: RouteType,
  title: string
): EscapeRouteStep[] {
  switch (routeType) {
    case "consulting_training":
      return buildConsultingTrainingSteps(title);
    case "cashback_partner":
      return buildCashbackPartnerSteps();
    case "on_site_service":
      return buildOnSiteServiceSteps();
    case "remote_service":
      return buildRemoteServiceSteps(title);
    case "freelance_project":
      return buildFreelanceProjectSteps(title);
    case "resale_trade":
      return buildResaleTradeSteps();
    case "simple_side_job":
      return buildSimpleSideJobSteps(title);
    case "generic":
    default:
      return buildGenericSafeSteps(title);
  }
}
